"""

# ---------------------------------------------------------------------------
# IndexedDB injection — origin-aware, same approach as generate-anti-detect.py
# ---------------------------------------------------------------------------
idb_data = data.get("indexedDB")
if idb_data and isinstance(idb_data, dict) and any(idb_data.values()):
    idb_json = json.dumps(idb_data)
    js += f"""
