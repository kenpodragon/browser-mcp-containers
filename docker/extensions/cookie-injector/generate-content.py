#!/usr/bin/env python3
"""Generate content.js with browser signature patches + IndexedDB injection from storageState.json."""
import json, sys, os

storage_path = sys.argv[1] if len(sys.argv) > 1 else "/app/storageState.json"
output_path = sys.argv[2] if len(sys.argv) > 2 else "/app/extensions/cookie-injector/content.js"

# Load full storageState
data = {}
sig = {}
try:
    with open(storage_path) as f:
        data = json.load(f)
        sig = data.get("browserSignature", {})
except Exception as e:
    print(f"WARNING: Could not read storageState: {e}", file=sys.stderr)

# Extract values with defaults
